    // time: O(n), space: O(n)
    override fun solveFirstPuzzle(): Int {
        val visited = hashSetOf<Int>()
        var headX = 0
        var headY = 0
        var tailX = 0
        var tailY = 0
        for ((dir, dist) in input) {
            repeat(dist) {
                when (dir) {
                    U -> headY--
                    R -> headX++
                    D -> headY++
                    L -> headX--
                }
                if (abs(headX - tailX) > 1 || abs(headY - tailY) > 1) {
                    tailX += (headX - tailX).sign
                    tailY += (headY - tailY).sign
                }
                visited += tailX * 10007 + tailY
            }
        }
        return visited.size
    }

